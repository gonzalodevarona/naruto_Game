package model;

public class Jutsu {
	
	private String name;
	private int factor;
	private Jutsu next;
	public Jutsu(String name, int factor) {
		super();
		this.name = name;
		this.factor = factor;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public int getFactor() {
		return factor;
	}
	public void setFactor(int factor) {
		this.factor = factor;
	}
	public Jutsu getNext() {
		return next;
	}
	public void setNext(Jutsu next) {
		this.next = next;
	}
	
	
	

}
